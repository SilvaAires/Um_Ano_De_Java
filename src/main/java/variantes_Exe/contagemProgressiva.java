package variantes_Exe;

public class contagemProgressiva {
    public static void main(String[] args) {
        System.out.println("Contagem Progressiva: ");
        int i = 1;
        while (i <= 10){
            System.out.print(i + " ");
            i++;
        }
        System.out.println("");
        System.out.println("Contagem Regressiva: ");
        int j = 10;
        while (j >= 1){
            System.out.print(j + " ");
            j--;
        }
    }
}
